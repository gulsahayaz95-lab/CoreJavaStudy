package Mart25;

public class AccessModifiersKonusu {

    /**
     * Access Modifiers :
     * 1-public  -> ile bir değişkeni, sınıfı veya methodu tanımlarsam projenin her yerinden bunlara erişebiliriz.

     * 2-private -> ile bir değişkeni, sınıfı veya methodu tanımlarsam sadece aynı class içinden erişebiliriz.

     * 3-protected -> ile aynı paket içerisinden erişebilir, farklı paketten erişebilmek için;
     * kalıtım yolu ile miras bırakmış olması gerekir.

     * 4-default -> herhangi bir modifier yazmazsak java default algılar,
     * default olanlara sadece aynı paket içerisinden erişilir.

     */














}
