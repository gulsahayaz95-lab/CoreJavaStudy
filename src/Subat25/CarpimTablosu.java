package Subat25;

public class CarpimTablosu {
    public static void main(String[] args) {

        /***
         * 1-5 arası çarpım tablosunu ekrana yazdır.
          */


        for (int sol = 1;  sol < 6; sol++) {

            for (int sag = 1; sag < 6; sag++) {

                System.out.printf("%4d", sol * sag );


            }
            System.out.println();
        }

















    }
}
